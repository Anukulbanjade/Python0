#Loops in python
# 1.while Loop
print("1. While Loops")
n=int(input("Enter The number: "))
i=0
while i<n:
    print(i)
    i+=1
print("Finished Iteration") 

#Example of While Loops
Input1=int(input("Enter the number you want to perform multiplication: "))
print(f"The multiplication table of {Input1}  is ")
j=1
while j<=10:
    print(Input1*j)
    j+=1

